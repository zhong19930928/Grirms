package com.yunhu.yhshxc.activity.carSales.bo;

/**
 * 产品库存
 * @author jishen
 *
 */
public class CarSalesStock {
	private int id;
	private int productId;//产品ID
	private int unitId;//单位ID
	private String unit;//单位名称
	private String productName;//产品名称 从第一级一直穿透到单品级别
	private double stockNum;//库存数
	private double stockoutNum;//缺货数
	private double replenishmentNum;//补货数
	
	
	
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public int getUnitId() {
		return unitId;
	}
	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public double getStockNum() {
		return stockNum;
	}
	public void setStockNum(double stockNum) {
		this.stockNum = stockNum;
	}
	public double getStockoutNum() {
		return stockoutNum;
	}
	public void setStockoutNum(double stockoutNum) {
		this.stockoutNum = stockoutNum;
	}
	public double getReplenishmentNum() {
		return replenishmentNum;
	}
	public void setReplenishmentNum(double replenishmentNum) {
		this.replenishmentNum = replenishmentNum;
	}

	
}
