package com.yunhu.yhshxc.workplan.bo;

/**
 * 
 * @author qingli 工作计划的实体类 封装工作计划的所有数据
 */
public class WorkPlanModle {
    
	private int planId;// 计划的唯一标识
	private String planTitle;// 计划标题

	private String planContent;// 计划内容
	private String planMark;// 计划备注
	private int importantLevel;// 重要等级
	private int rushLevel;// 紧急等级
	private String planTime;//计划的时间段
	private int planSort;//暂时记录计划的编号,无实际用途
	

	public int getPlanSort() {
		return planSort;
	}
	public void setPlanSort(int planSort) {
		this.planSort = planSort;
	}
	public int getPlanId() {
		return planId;
	}
	public void setPlanId(int planId) {
		this.planId = planId;
	}
	public String getPlanTitle() {
		return planTitle;
	}
	public void setPlanTitle(String planTitle) {
		this.planTitle = planTitle;
	}
	public String getPlanContent() {
		return planContent;
	}
	public void setPlanContent(String planContent) {
		this.planContent = planContent;
	}
	public String getPlanMark() {
		return planMark;
	}
	public void setPlanMark(String planMark) {
		this.planMark = planMark;
	}
	public int getImportantLevel() {
		return importantLevel;
	}
	public void setImportantLevel(int importantLevel) {
		this.importantLevel = importantLevel;
	}
	public int getRushLevel() {
		return rushLevel;
	}
	public void setRushLevel(int rushLevel) {
		this.rushLevel = rushLevel;
	}
	public String getPlanTime() {
		return planTime;
	}
	public void setPlanTime(String planTime) {
		this.planTime = planTime;
	}
	
	


  
}
