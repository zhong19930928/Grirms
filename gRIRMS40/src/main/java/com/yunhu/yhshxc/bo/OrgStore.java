package com.yunhu.yhshxc.bo;

public class OrgStore {
	private int id;//主键
	private int storeId;//店面ID
	private int orgId;//机构ID
	private String storeName;//店面名称
	private Double storeLon;//店面经度
	private Double storeLat;//店面纬度
	private String orgCode;//店面机构穿透
	private int level;//客户级别
	
	
	
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStoreId() {
		return storeId;
	}
	public void setStoreId(int storeId) {
		this.storeId = storeId;
	}
	public int getOrgId() {
		return orgId;
	}
	public void setOrgId(int orgId) {
		this.orgId = orgId;
	}
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public Double getStoreLon() {
		return storeLon;
	}
	public void setStoreLon(Double storeLon) {
		this.storeLon = storeLon;
	}
	public Double getStoreLat() {
		return storeLat;
	}
	public void setStoreLat(Double storeLat) {
		this.storeLat = storeLat;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	
	
	
	
	
	
}
