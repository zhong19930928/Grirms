package com.yunhu.yhshxc.bo;

public class OrgUser {
	
	private int id;//主键
	private Integer userId;//用户ID
	private Integer orgId;//机构ID
	private String userName;//用户名称
	private Integer pid;//上层ID
	private String purview;//用户的targetid
	private Integer authSearch;//下发的时候查询用户穿透的类型
	private String authOrgId;//指定多个区域的orgId
	private String orgCode;//机构层级code
	private int roleId;//角色ID
	private String roleName;//角色名称
	
	
	
	public int getRoleId() {
		return roleId;
	}
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getOrgId() {
		return orgId;
	}
	public void setOrgId(Integer orgId) {
		this.orgId = orgId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPurview() {
		return purview;
	}
	public void setPurview(String purview) {
		this.purview = purview;
	}
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}

	public Integer getAuthSearch() {
		return authSearch;
	}
	public void setAuthSearch(Integer authSearch) {
		this.authSearch = authSearch;
	}
	public String getAuthOrgId() {
		return authOrgId;
	}
	public void setAuthOrgId(String authOrgId) {
		this.authOrgId = authOrgId;
	}
}
