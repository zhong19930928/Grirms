package com.yunhu.yhshxc.bo;
/**
 * 报表模块管理者本月出勤情况数据实体类
 * @author qingli
 *
 */
public class ThisMonthAttendance {
    /**
     * 正常考勤人次
     */
	private int normal;
	/**
	 * 迟到早退人次
	 */
	private int late;
	/**
	 * 请假人次
	 */
	private int leaveof;
	/**
	 * 其他人次
	 */
	private int other;
	/**
	 * 休息人次
	 */
	private int rest;
	/**
	 * 加班人次
	 */
	private int overtime;
	/**
	 * 
	 * 正常出勤率
	 */
	private String average;
	
	public String getAverage() {
		return average;
	}
	public void setAverage(String average) {
		this.average = average;
	}
	public int getNormal() {
		return normal;
	}
	public void setNormal(int normal) {
		this.normal = normal;
	}
	public int getLate() {
		return late;
	}
	public void setLate(int late) {
		this.late = late;
	}
	public int getLeaveof() {
		return leaveof;
	}
	public void setLeaveof(int leaveof) {
		this.leaveof = leaveof;
	}
	public int getOther() {
		return other;
	}
	public void setOther(int other) {
		this.other = other;
	}
	public int getRest() {
		return rest;
	}
	public void setRest(int rest) {
		this.rest = rest;
	}
	public int getOvertime() {
		return overtime;
	}
	public void setOvertime(int overtime) {
		this.overtime = overtime;
	}
	
	
}
