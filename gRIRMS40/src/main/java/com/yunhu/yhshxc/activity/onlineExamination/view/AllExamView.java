package com.yunhu.yhshxc.activity.onlineExamination.view;

import com.yunhu.yhshxc.R;

import android.content.Context;
import android.view.View;
import android.widget.TextView;

public class AllExamView {
	private View view;
	private Context context;
	private TextView online_title;
	private TextView online_time;
	private TextView online_explian;
	public AllExamView(Context context){
		this.context = context;
		view = View.inflate(context, R.layout.online_exam_all_item, null);
		online_title = (TextView) view.findViewById(R.id.online_title);
		online_time = (TextView) view.findViewById(R.id.online_time);
		online_explian = (TextView) view.findViewById(R.id.online_explian);
	}
	public View getView(){
		return view;
	}
}
