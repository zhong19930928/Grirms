package com.yunhu.yhshxc.order.bo;

public class OrderItem {

	private int id; //唯一
	private int productId;
	private String name;// 货物名称
	private long total;// 总数
	private double price;// 单价
	private int state;// 订单状态
	private long arrivalQuantity; // 到货数量
	private long newArrivalQuantity; // 新到货数量，用户输入的
	private long sales;// 销量
	
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public long getArrivalQuantity() {
		return arrivalQuantity;
	}

	public void setArrivalQuantity(long arrivalQuantity) {
		this.arrivalQuantity = arrivalQuantity;
	}

	public long getNewArrivalQuantity() {
		return newArrivalQuantity;
	}

	public void setNewArrivalQuantity(long newArrivalQuantity) {
		this.newArrivalQuantity = newArrivalQuantity;
	}

	public long getSales() {
		return sales;
	}

	public void setSales(long sales) {
		this.sales = sales;
	}

	@Override
	public String toString() {
		return name == null ? "" : name;
	}

}
