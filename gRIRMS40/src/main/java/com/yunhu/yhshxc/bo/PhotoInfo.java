package com.yunhu.yhshxc.bo;

import java.io.Serializable;

public class PhotoInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -1651797723192023021L;
	private String id;
	private String photoName;//照片名称
	private String photoUrl;//照片下载url
	private String photoSize;//照片大小
	private String photoTitle;//照片标题
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPhotoName() {
		return photoName;
	}
	public void setPhotoName(String photoName) {
		this.photoName = photoName;
	}
	public String getPhotoUrl() {
		return photoUrl;
	}
	public void setPhotoUrl(String photoUrl) {
		this.photoUrl = photoUrl;
	}
	public String getPhotoSize() {
		return photoSize;
	}
	public void setPhotoSize(String photoSize) {
		this.photoSize = photoSize;
	}
	public String getPhotoTitle() {
		return photoTitle;
	}
	public void setPhotoTitle(String photoTitle) {
		this.photoTitle = photoTitle;
	}
	
	
}
