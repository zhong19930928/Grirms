package com.yunhu.yhshxc.bo;

public class VisitStore {
	private int id;//主键
	private int storeId;//店面ID
	private String name;//店面名称
	private int wayId;//线路ID
	private int targetid;//数据ID
	private int isCheckin;//是否要进店定位
	private int isCheckout;//是否要离店定位
	private String submitDate;//访店提交时间
	private int planId;//计划ID
	private String deleteDate;//删除店面的时间
	private String lon;
	private String lat;
	private int isCheck;//是否要验证店面距离
	private int storeDistance;//店面距离
	private String locType;//定位方式 1仅GPS 2所有 默认为2
	private String isAddress;//显示地址详细 0不显示 1显示 默认为1
	private String isNewLoc;//超出范围要重新定位 0不需要重新定位 1需要重新定位 默认为0
	private int isNoWait;//是否无等待提交 0不需要无等待(前端提交)  1 需要无等待（后台提交）
	private int submitNum;//已经提交的次数
	
	public int getSubmitNum() {
		return submitNum;
	}
	public void setSubmitNum(int submitNum) {
		this.submitNum = submitNum;
	}
	public int getIsNoWait() {
		return isNoWait;
	}
	public void setIsNoWait(int isNoWait) {
		this.isNoWait = isNoWait;
	}
	public String getLocType() {
		return locType;
	}
	public void setLocType(String locType) {
		this.locType = locType;
	}
	public String getIsAddress() {
		return isAddress;
	}
	public void setIsAddress(String isAddress) {
		this.isAddress = isAddress;
	}
	public String getIsNewLoc() {
		return isNewLoc;
	}
	public void setIsNewLoc(String isNewLoc) {
		this.isNewLoc = isNewLoc;
	}
	public int getIsCheck() {
		return isCheck;
	}
	public void setIsCheck(int isCheck) {
		this.isCheck = isCheck;
	}
	public int getStoreDistance() {
		return storeDistance;
	}
	public void setStoreDistance(int storeDistance) {
		this.storeDistance = storeDistance;
	}
	public String getLon() {
		return lon;
	}
	public void setLon(String lon) {
		this.lon = lon;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	
	public String getDeleteDate() {
		return deleteDate;
	}
	public void setDeleteDate(String deleteDate) {
		this.deleteDate = deleteDate;
	}
	public String getSubmitDate() {
		return submitDate;
	}
	public void setSubmitDate(String submitDate) {
		this.submitDate = submitDate;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getStoreId() {
		return storeId;
	}
	public void setStoreId(int storeId) {
		this.storeId = storeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getWayId() {
		return wayId;
	}
	public void setWayId(int wayId) {
		this.wayId = wayId;
	}
	public int getTargetid() {
		return targetid;
	}
	public void setTargetid(int targetid) {
		this.targetid = targetid;
	}
	public int getIsCheckin() {
		return isCheckin;
	}
	public void setIsCheckin(int isCheckin) {
		this.isCheckin = isCheckin;
	}
	public int getIsCheckout() {
		return isCheckout;
	}
	public void setIsCheckout(int isCheckout) {
		this.isCheckout = isCheckout;
	}
	public int getPlanId() {
		return planId;
	}
	public void setPlanId(int planId) {
		this.planId = planId;
	}
}
